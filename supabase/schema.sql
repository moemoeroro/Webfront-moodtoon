create or replace function public.toggle_comment_like(p_comment_id bigint)
returns table(
  comment_id bigint,
  liked boolean,
  empathy_count integer,
  liked_comment_ids jsonb
)
language plpgsql
security definer
set search_path = public
as $$
declare
  current_user_id uuid := auth.uid();
  next_liked boolean;
  next_count integer;
begin
  if current_user_id is null then
    raise exception 'Authentication required';
  end if;

  if exists (
    select 1
    from public.comment_likes
    where comment_likes.comment_id = p_comment_id
      and comment_likes.user_id = current_user_id
  ) then
    delete from public.comment_likes
    where comment_likes.comment_id = p_comment_id
      and comment_likes.user_id = current_user_id;

    update public.comments as c
    set empathy_count = greatest(c.empathy_count - 1, 0)
    where c.id = p_comment_id
    returning c.empathy_count into next_count;

    next_liked := false;
  else
    insert into public.comment_likes (comment_id, user_id)
    values (p_comment_id, current_user_id);

    update public.comments as c
    set empathy_count = c.empathy_count + 1
    where c.id = p_comment_id
    returning c.empathy_count into next_count;

    next_liked := true;
  end if;

  update public.profiles
  set liked_comments = coalesce(
    (
      select jsonb_agg(comment_likes.comment_id order by comment_likes.created_at desc)
      from public.comment_likes
      where comment_likes.user_id = current_user_id
    ),
    '[]'::jsonb
  )
  where profiles.id = current_user_id
  returning profiles.liked_comments into liked_comment_ids;

  return query
  select p_comment_id, next_liked, next_count, liked_comment_ids;
end;
$$;

grant execute on function public.toggle_comment_like(bigint) to authenticated;
